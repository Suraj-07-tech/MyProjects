public class Array13 {
    public static void main(String[] args)
    {
        int n=5;
        for(int i=1;i<=5;i++)
        {
            for(int j=n;j<=5;j++)
            {
                System.out.print(j+" ");
                
            }
            n--;
            System.out.println();
        }
    }
}
