public class Assigment_Operator {
    public static void main(String[] args) {
        long salary=20000000;
        salary+=100000;
        System.out.println(salary);
        salary-=100000;
        System.out.println(salary);
        salary*=2;
        System.out.println(salary);
        salary/=10000; 
        System.out.println(salary);
        salary%=100;
        System.out.println(salary);
    }
}
